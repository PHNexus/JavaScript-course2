// Operadores Aritimeticos(matematicos)
// Operadores Atribuicao
// Operadores de comparacao
// Operadores Logicos
// Operadores Bitwise

let salario = 100;

console.log(salario + salario); // adicao
console.log(salario - salario); // subtracao
console.log(salario * salario); // multiplacao
console.log(salario / salario); // divisao
console.log(5 ** 5); // expontencial // potencia

// ++ --  operadores de incliento e decremeto

let idade = 16;
console.log(++idade); // incrementar +1
console.log(--idade); // descrementar / diminuir -1
// 16 + 1 = 17   17-1 =16

//sempre use === sera comparado para ser identico
// operadores de igualdade
// Igualdade estria

console.log(1 === 1); // esta comparando e o tipo tem q ser identico
console.log("1" === 1); // tipo diferente

// igualdade solta
console.log("1" == 1); // vai comparar apenas o numero = true
