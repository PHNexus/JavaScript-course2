// criar uma funcao que faz soma
function somarDoisValores(valor) {
  return valor + 2;
}
console.log(somarDoisValores(5)); // ira somar 2 + 5 retornando 7

// agora criar uma funcao que multiplica por o 7 por 2
function multiplicarPorDois(valor) {
  return valor * 2;
}
console.log(multiplicarPorDois(7)); // ira retornar o 7 multiplicado por 2 retornando 14

// funcao que retornar um valor multiplicado por 3 utilizando variavel
function multiplicadoPorTres(valor) {
  return valor * 3;
}

let resultado = multiplicadoPorTres(10); // 10 x 3 = 30
console.log(resultado); // ira retornar 30
